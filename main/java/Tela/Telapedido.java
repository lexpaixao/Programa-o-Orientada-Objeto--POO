    private void FecharcontaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FecharcontaActionPerformed
        // TODO add your handling code here:
        pedido.d=pedido.s+pedido.b;
        if(pedido.svalor>0 && pedido.d==2){            
        if(pedido.svalor<50){
        JOptionPane.showMessageDialog(null,"Cliente= "+pedido.cliente+"\nMesa= "+pedido.mesa+"\nQuantidades de itens pedidos= "+pedido.x+"\nO valor total = "+df.format(pedido.svalor)+"\n Valor Insuficiente para parcelar");    
          pedido.svalor=0;
        }
        else if(pedido.svalor>50 && pedido.svalor<=80){
        JOptionPane.showMessageDialog(null,"Cliente= "+pedido.cliente+"\nMesa= "+pedido.mesa+"\nQuantidades de itens pedidos= "+pedido.x+"\nO valor total = "+df.format(pedido.svalor)+"\n Valor pode ser parcelado em ate 2x de: "+df.format(pedido.svalor/2));    
          pedido.svalor=0;
        }
        else if(pedido.svalor>80 && pedido.svalor<=150){
        JOptionPane.showMessageDialog(null,"Cliente= "+pedido.cliente+"\nMesa= "+pedido.mesa+"\nQuantidades de itens pedidos= "+pedido.x+"\nO valor total = "+df.format(pedido.svalor)+"\n Valor pode ser parcelado em ate 3x de: "+df.format(pedido.svalor/3));    
          pedido.svalor=0;
        }
        else if(pedido.svalor>150){
        JOptionPane.showMessageDialog(null,"Cliente= "+pedido.cliente+"\nMesa= "+pedido.mesa+"\nQuantidades de itens pedidos= "+pedido.x+"\nO valor total = "+df.format(pedido.svalor)+"\n Valor pode ser parcelado em ate 5x de: "+df.format(pedido.svalor/5));    
          pedido.svalor=0;
        }
         } 
       else if(pedido.svalor>0 && pedido.b!=2){
        
           if(pedido.formapagar=="Dinheiro"){
       String pago=JOptionPane.showInputDialog("Informe a quantia para pagamento\n Valor tem que ser maior ou igual a: "+df.format(pedido.svalor));
       pedido.vpago=Double.parseDouble(pago);
       while(pedido.vpago<pedido.svalor)   {
       
       pago=JOptionPane.showInputDialog("Informe a quantia para pagamento\n Valor tem que ser maior ou igual a: "+pedido.svalor);
       pedido.vpago=Double.parseDouble(pago);
       
                    }
       
       pedido.troco=pedido.vpago-pedido.svalor;
      JOptionPane.showMessageDialog(null,"Cliente= "+pedido.cliente+"\nMesa= "+pedido.mesa+"\nQuantidades de itens pedidos= "+pedido.x+"\nO valor total = "+df.format(pedido.svalor)+"\n Forma de Pagamento= "+pedido.formapagar+"\n Valor pago= "+df.format(pedido.vpago)+"\n Troco= "+df.format(pedido.troco));
       }
       
      else{
            JOptionPane.showMessageDialog(null,"Cliente= "+pedido.cliente+"\nMesa= "+pedido.mesa+"\nQuantidades de itens pedidos= "+pedido.x+"\nO valor total = "+df.format(pedido.svalor)+"\n Forma de Pagamento= "+pedido.formapagar);
            pedido.svalor=0;
           
         }
           
           
        }
        else{
           
            JOptionPane.showMessageDialog(null,"Houve um erro inesperado!\n"+pedido.svalor+"\n");
        }       
        setVisible(false);
         java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                new Telainicio().setVisible(true);
            }
        });
    }//GEN-LAST:event_FecharcontaActionPerformed

