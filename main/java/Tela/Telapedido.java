    private void RealizarpedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RealizarpedidoActionPerformed
        try{
        pedido.a = Integer.parseInt(codigo.getText()); 
        pedido.qnt = Integer.parseInt(qntd.getText()); 
        
        if(parcelar.isSelected()){
        pedido.parcelar=parcelar.getText();
        pedido.s=1;
        }
        if (pix.isSelected()){
            pedido.formapagar = pix.getText();
               pedido.b=0;
        } else if (deb.isSelected()) {
            pedido.formapagar = deb.getText();
               pedido.b=0;
        } else if (cre.isSelected()) {
            pedido.formapagar = cre.getText();
            pedido.b=1;
        } else if (money.isSelected()) {
            pedido.formapagar = money.getText();
               pedido.b=0;
        }

        if (pedido.a > 0 && pedido.qnt > 0 && pedido.formapagar != null) {
            pedido.x+=pedido.qnt;
            PedidoDAO pedidoDAO = new PedidoDAO();
            Cliente pedidoDoBanco = pedidoDAO.buscarPedidoPorId(pedido.a);

            if (pedidoDoBanco != null) {
                
                for(pedido.p=0;pedido.p<pedido.qnt;pedido.p++){
                    pedido.svalor += pedidoDoBanco.svalor;
                    pedido.preco=pedidoDoBanco.svalor;
                    }
                if (pedido.p == pedido.qnt) {
                        JOptionPane.showMessageDialog(null, "Cliente= " + pedido.cliente
                                + "\nMesa= " + pedido.mesa 
                                + "\nPedido = " + pedidoDoBanco.produto 
                                + "\nQuantidade = " + pedido.qnt 
                                + "\n Valor por item= " + df.format(pedido.preco)
                                + "\n Valor total= " + df.format(pedido.svalor)); 
            
                 ConexaoBDH.inserirHistorico(pedido.cliente, pedido.mesa, pedidoDoBanco.produto, pedido.qnt,pedido.preco, pedido.svalor,pedido.formapagar);
                                }


            } else {
                JOptionPane.showMessageDialog(null, " Codigo invalido");
            }
        }  else if( pedido.formapagar == null){
        JOptionPane.showMessageDialog(null, "\n Forma de Pagamento NAO INFORMADO!");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Erro de formato. Certifique-se de que a quantidade é um número válido.");
    }
    }//GEN-LAST:event_RealizarpedidoActionPerformed

